    public void xuLySuaTapPhim() {
        //lấy dữ liệu nhập vào
        String tenTap = tfTenTap.getText();
        int tap = cbTap.getSelectedIndex() + 1;
        String urlVideo = tfUrlVideo.getText();
        
        tapPhim.setTenTap(tenTap);
        tapPhim.setStt(tap);
        tapPhim.setUrlVideo(urlVideo);
        
        //kiểm tra dữ liệu nhập vào
        if (tenTap.isEmpty() || urlVideo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đây đủ thông tin");
            return; //Dừng xử lý
        }
        
        //check tập phim
        if (TapPhim.checkTapPhimSua(tenTap, tap, boPhim.getId())) {
            JOptionPane.showMessageDialog(null, "Tập phim đã tồn tại.");
            return; //Dừng xử lý
        }
        
        //Sửa tập phim
        boolean kqSua = tapPhim.suaTapPhim();
        if(kqSua) {
            JOptionPane.showMessageDialog(null, "Đã sửa.");
        }
        else {
            JOptionPane.showMessageDialog(null, "Sửa thất bại.");
        }
        
        
    }
    
