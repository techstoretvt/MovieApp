    public void ganGiaTriCombobox() {
        int soTap = boPhim.getSoTap();
        ArrayList<String> itemsList = new ArrayList<>();
        for (int i = 1; i <= soTap; i++) {
            itemsList.add("" + i);
        }
        String[] items = itemsList.toArray(new String[0]);

        // Tạo một ComboBoxModel từ mảng items
        ComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>(items);
        cbTap.setModel(comboBoxModel);
        cbTap.setSelectedIndex(tapPhim.getStt() - 1);
    }

