    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
        // TODO add your handling code here:
        //lấy frame cha
        JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(ItemPhimAdmin.this);
        
        //mở màn hình sửa bộ phim
        SuaBoPhim manHinhSuaBoPhim = new SuaBoPhim(boPhim);
        manHinhSuaBoPhim.setVisible(true); // cho hiển thị lên
        manHinhSuaBoPhim.setLocationRelativeTo(null); // cho nằm giữa màn hình
        
        // thêm sự kiện khi đóng màn hình
        manHinhSuaBoPhim.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e) {
                // hiện lại frame cha
                frame.setVisible(true);
                QuanLyPhim.layDSPhim();
            }
        });

        // ẩn frame cha
        frame.setVisible(false);
    }//GEN-LAST:event_btnSuaMouseClicked

