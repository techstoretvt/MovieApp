    public void xuLyXoaBoPhim() {
        StatusXoaPhim kqXoa = boPhim.xoaPhim();
        if(kqXoa == StatusXoaPhim.SUCCESS) {
            QuanLyPhim.layDSPhim();
        }
        else if(kqXoa == StatusXoaPhim.ERRORR) {
            JOptionPane.showMessageDialog(null, "Xóa phim thất bại.");
        }
        else {
            JOptionPane.showMessageDialog(null, "Xóa phim thành công nhưng xóa tập phim thất bại.");
        }
        
    }

